// Parsers
// ---------------------------------------------------------------------------

export function parseCaptionBody(body: string): RawCaptionSegment[] {
  const trimmed = body.trim();
  if (!trimmed) return [];

  if (trimmed.startsWith("WEBVTT")) return parseVtt(trimmed);

  if (trimmed.startsWith("{")) {
    try {
      return parseJson3(JSON.parse(trimmed) as Json3Captions);
    } catch {
      // fall through
    }
  }

  if (trimmed.startsWith("<") || trimmed.includes("<text")) {
    return parseXmlCaptions(trimmed);
  }

  try {
    return parseJson3(JSON.parse(trimmed) as Json3Captions);
  } catch {
    return [];
  }
}

function parseJson3(data: Json3Captions): RawCaptionSegment[] {
  const segments: RawCaptionSegment[] = [];
  for (const event of data.events ?? []) {
    if (!event.segs?.length) continue;
    const text = event.segs
      .map((s) => s.utf8 ?? "")
      .join("")
      .replace(/\n+/g, " ")
      .replace(/\s+/g, " ")
      .trim();
    if (!text) continue;
    const startTime = (event.tStartMs ?? 0) / 1000;
    const endTime = startTime + (event.dDurationMs ?? 0) / 1000;
    segments.push({
      startTime,
      endTime: endTime > startTime ? endTime : startTime,
      text: decodeHtmlEntities(text),
    });
  }
  return segments;
}

function parseXmlCaptions(xml: string): RawCaptionSegment[] {
  const doc = new DOMParser().parseFromString(xml, "text/xml");
  if (doc.querySelector("parsererror")) return [];

  const nodes = doc.querySelectorAll("text");
  const segments: RawCaptionSegment[] = [];

  nodes.forEach((node) => {
    const start = parseFloat(node.getAttribute("start") ?? "0");
    const dur = parseFloat(node.getAttribute("dur") ?? "0");
    const text = decodeHtmlEntities(node.textContent ?? "")
      .replace(/\n+/g, " ")
      .replace(/\s+/g, " ")
      .trim();
    if (!text || Number.isNaN(start)) return;
    segments.push({
      startTime: start,
      endTime: !Number.isNaN(dur) && dur > 0 ? start + dur : start,
      text,
    });
  });

  return segments;
}

function parseVtt(vtt: string): RawCaptionSegment[] {
  const segments: RawCaptionSegment[] = [];
  const blocks = vtt.replace(/\r\n/g, "\n").split(/\n\n+/);

  for (const block of blocks) {
    const lines = block.split("\n").filter((l) => l.trim().length > 0);
    const timeIdx = lines.findIndex((l) => l.includes("-->"));
    if (timeIdx === -1) continue;
    const [startRaw, endRaw] = lines[timeIdx].split("-->").map((s) => s.trim());
    const startTime = parseVttTimestamp(startRaw.split(/\s+/)[0]);
    const endTime = parseVttTimestamp(endRaw.split(/\s+/)[0]);
    if (startTime === null || endTime === null) continue;
    const text = lines
      .slice(timeIdx + 1)
      .join(" ")
      .replace(/<[^>]+>/g, "")
      .replace(/\s+/g, " ")
      .trim();
    if (!text) continue;
    segments.push({
      startTime,
      endTime,
      text: decodeHtmlEntities(text),
    });
  }
  return segments;
}

function parseVttTimestamp(ts: string): number | null {
  const parts = ts.split(":");
  if (parts.length < 2 || parts.length > 3) return null;
  let hours = 0;
  let minutes = 0;
  let seconds = 0;
  if (parts.length === 3) {
    hours = parseInt(parts[0], 10);
    minutes = parseInt(parts[1], 10);
    seconds = parseFloat(parts[2]);
  } else {
    minutes = parseInt(parts[0], 10);
    seconds = parseFloat(parts[1]);
  }
  if ([hours, minutes, seconds].some((n) => Number.isNaN(n))) return null;
  return hours * 3600 + minutes * 60 + seconds;
}

function decodeHtmlEntities(text: string): string {
  // textarea trick handles the full entity set in the browser
  if (typeof document !== "undefined") {
    const el = document.createElement("textarea");
    el.innerHTML = text;
    return el.value;
  }
  return text
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, '"')
    .replace(/&#39;/g, "'")
    .replace(/&apos;/g, "'")
    .replace(/&#(\d+);/g, (_, n: string) =>
      String.fromCharCode(parseInt(n, 10))
    )
    .replace(/&#x([0-9a-fA-F]+);/g, (_, h: string) =>
      String.fromCharCode(parseInt(h, 16))
    )
    .replace(/\\u0026/g, "&");
}

function stringifyErr(err: unknown): string {
  if (err instanceof Error) return err.message;
  return String(err);
}

async function hashCaptionTrack(track: CaptionTrackInfo): Promise<string> {
  const material = [
    track.languageCode,
    track.isAutoGenerated ? "asr" : "manual",
    track.name,
  ].join("|");
  const data = new TextEncoder().encode(material);
  const digest = await crypto.subtle.digest("SHA-256", data);
  return Array.from(new Uint8Array(digest))
    .map((b) => b.toString(16).padStart(2, "0"))
    .join("")
    .slice(0, 16);
}
