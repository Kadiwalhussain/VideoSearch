async function resolveInnertubeApiKey(videoId: string): Promise<string> {
  // 1) MAIN-world bridge (ytcfg on the live page)
  const fromBridge = await requestFromPageBridge<{ apiKey?: string | null }>(
    "GET_INNERTUBE_KEY",
    1500
  );
  if (fromBridge?.apiKey) return fromBridge.apiKey;

  // 2) Inline scripts already in the DOM
  const fromDom = extractApiKeyFromHtml(document.documentElement.innerHTML);
  if (fromDom) return fromDom;

  // 3) Fetch watch HTML
  try {
    const html = await fetchWatchHtml(videoId);
    const fromHtml = extractApiKeyFromHtml(html);
    if (fromHtml) return fromHtml;
  } catch (err) {
    console.warn("[VideoSearch AI] watch HTML fetch for API key failed:", err);
  }

  console.warn(
    "[VideoSearch AI] using fallback INNERTUBE_API_KEY (page key not found)"
  );
  return FALLBACK_INNERTUBE_KEY;
}

function extractApiKeyFromHtml(html: string): string | null {
  const m = html.match(/"INNERTUBE_API_KEY"\s*:\s*"([a-zA-Z0-9_-]+)"/);
  return m?.[1] ?? null;
}

async function fetchWatchHtml(videoId: string): Promise<string> {
  const res = await fetch(
    `https://www.youtube.com/watch?v=${encodeURIComponent(videoId)}`,
    {
      credentials: "include",
      headers: { Accept: "text/html", "Accept-Language": "en-US" },
    }
  );
  if (!res.ok) {
    throw new TranscriptFetchError(
      `Watch page HTTP ${res.status} for ${videoId}`
    );
  }
  // Decode HTML entities similar to youtube-transcript-api (unescape)
  return decodeHtmlEntities(await res.text());
}

async function fetchInnertubePlayer(
  videoId: string,
  apiKey: string
): Promise<YtPlayerResponse> {
  const url = `https://www.youtube.com/youtubei/v1/player?key=${encodeURIComponent(apiKey)}`;

  let res: Response;
  try {
    res = await fetch(url, {
      method: "POST",
      credentials: "include",
      headers: {
        "Content-Type": "application/json",
        "Accept-Language": "en-US",
      },
      body: JSON.stringify({
        context: INNERTUBE_CONTEXT,
        videoId,
      }),
    });
  } catch (err) {
    throw new TranscriptFetchError(
      `Innertube network error: ${stringifyErr(err)}`,
      { cause: err }
    );
  }

  if (!res.ok) {
    const text = await res.text().catch(() => "");
    throw new TranscriptFetchError(
      `Innertube player HTTP ${res.status}: ${text.slice(0, 120)}`
    );
  }

  try {
    return (await res.json()) as YtPlayerResponse;
  } catch (err) {
    throw new TranscriptFetchError("Innertube returned non-JSON body", {
      cause: err,
    });
  }
}

function assertPlayable(player: YtPlayerResponse, videoId: string): void {
  const status = player.playabilityStatus?.status;
  if (!status || status === "OK") return;

  const reason = player.playabilityStatus?.reason ?? status;
  if (status === "LOGIN_REQUIRED") {
    throw new TranscriptFetchError(
      `YouTube requires login for this video (${reason}). Open the video while signed in and retry.`
    );
  }
  if (status === "UNPLAYABLE" || status === "ERROR") {
    // Still allow captions if present; only throw if we have nothing later
    console.warn(
      "[VideoSearch AI] playability",
      status,
      reason,
      "— continuing if captions exist"
    );
    return;
  }
  console.warn("[VideoSearch AI] playability status:", status, reason, videoId);
}

// ---------------------------------------------------------------------------
// Page bridge helper
// ---------------------------------------------------------------------------

function requestFromPageBridge<T>(
  type: string,
  timeoutMs: number
): Promise<T | null> {
  return new Promise((resolve) => {
    const requestId = `${Date.now()}-${Math.random().toString(36).slice(2)}`;

    const onMessage = (event: MessageEvent) => {
      if (event.source !== window) return;
      const data = event.data;
      if (!data || data.source !== BRIDGE_SOURCE) return;
      if (data.type !== `${type}_RESULT`) return;
      if (data.requestId !== requestId) return;
      window.removeEventListener("message", onMessage);
      window.clearTimeout(timer);
      resolve((data.payload as T) ?? null);
    };

    window.addEventListener("message", onMessage);
    window.postMessage({ source: BRIDGE_SOURCE, type, requestId }, "*");

    const timer = window.setTimeout(() => {
      window.removeEventListener("message", onMessage);
      resolve(null);
    }, timeoutMs);
  });
}

// ---------------------------------------------------------------------------
// Track selection — use the video's default / original language
// (Hindi video → Hindi captions; English video → English captions)
// Do NOT force-translate the transcript. AI answers stay English separately.
// ---------------------------------------------------------------------------

/**
 * Pick the default caption track as YouTube would:
 * 1. Manually created tracks first (creator language), keep YouTube order
 * 2. Else auto-generated (ASR) first track (usually the spoken language)
 */
function selectDefaultCaptionTrack(tracks: YtCaptionTrack[]): YtCaptionTrack {
  if (tracks.length === 0) {
    throw new TranscriptFetchError("No caption tracks to select.");
  }

  const manual = tracks.filter((t) => t.kind !== "asr");
  if (manual.length > 0) {
    // YouTube usually lists the primary/default language first among manuals
    return manual[0];
  }

  // ASR only — first track is typically the spoken language (e.g. hi, en)
  return tracks[0];
}

function trackDisplayName(track: YtCaptionTrack): string {
  const name = track.name;
  if (typeof name === "string") return name;
  if (name && typeof name === "object") {
    if (name.simpleText) return name.simpleText;
    if (name.runs?.[0]?.text) return name.runs[0].text;
  }
  return track.languageCode ?? "unknown";
}

// ---------------------------------------------------------------------------
// Caption body fetch — never re-serialize signed URLs
// ---------------------------------------------------------------------------

function appendQueryParam(baseUrl: string, key: string, value: string): string {
  if (new RegExp(`[?&]${key}=`).test(baseUrl)) return baseUrl;
  const sep = baseUrl.includes("?") ? "&" : "?";
  return `${baseUrl}${sep}${key}=${value}`;
}

async function fetchCaptionBodyRobust(
  baseUrl: string
): Promise<{ body: string; usedLabel: string }> {
  const candidates: Array<{ url: string; label: string }> = [
    { url: appendQueryParam(baseUrl, "fmt", "json3"), label: "json3" },
    { url: baseUrl, label: "xml-default" },
    { url: appendQueryParam(baseUrl, "fmt", "srv3"), label: "srv3" },
    { url: appendQueryParam(baseUrl, "fmt", "vtt"), label: "vtt" },
  ];

  const errors: string[] = [];

  for (const { url, label } of candidates) {
    try {
      const res = await fetch(url, {
        credentials: "include",
        headers: { "Accept-Language": "en-US", Accept: "*/*" },
      });
      if (!res.ok) {
        errors.push(`${label}: HTTP ${res.status}`);
        continue;
      }
      const body = await res.text();
      if (!body.trim()) {
        errors.push(`${label}: empty body`);
        continue;
      }
      const t = body.trim();
      if (
        t.startsWith("{") ||
        t.startsWith("<") ||
        t.startsWith("WEBVTT")
      ) {
        return { body, usedLabel: label };
      }
      errors.push(`${label}: bad prefix ${JSON.stringify(t.slice(0, 30))}`);
    } catch (err) {
      errors.push(`${label}: ${stringifyErr(err)}`);
    }
  }

  throw new TranscriptFetchError(
    `Caption download failed. ${errors.join(" · ")}`
  );
}

