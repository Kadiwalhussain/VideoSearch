/**
 * Transcript acquisition — YouTube caption track only (no STT).
 *
 * Strategy (ported from the proven youtube-transcript-api approach):
 * 1. Read INNERTUBE_API_KEY from the watch page
 * 2. POST youtubei/v1/player with ANDROID client context
 *    → returns caption baseUrls that work WITHOUT PO tokens
 * 3. Prefer the video's default/native caption language (e.g. Hindi stays Hindi)
 *    Manual tracks first, then ASR — never force-translate the transcript.
 * 4. Fetch timedtext (never re-serialize signed URLs)
 * AI answers/topics stay English via LLM prompts (separate from caption language).
 *
 * Why not WEB player response / page captionTracks?
 * WEB tracks often include `&exp=xpe`, which requires a PO token and
 * returns empty bodies in the browser — that was the "Fetch failed" bug.
 */

import type { RawCaptionSegment } from "../types/schema";

// ---------------------------------------------------------------------------
// Public types
// ---------------------------------------------------------------------------

export interface CaptionTrackInfo {
  languageCode: string;
  name: string;
  isAutoGenerated: boolean;
  baseUrl: string;
}

export interface TranscriptFetchResult {
  videoId: string;
  segments: RawCaptionSegment[];
  captionTrackHash: string;
  track: CaptionTrackInfo;
}

export class NoCaptionsError extends Error {
  readonly code = "NO_CAPTIONS" as const;

  constructor(videoId: string, message?: string) {
    super(
      message ??
        `No captions available for video ${videoId}. Speech-to-text fallback is out of scope for Phase 1.`
    );
    this.name = "NoCaptionsError";
  }
}

export class TranscriptFetchError extends Error {
  readonly code = "TRANSCRIPT_FETCH_ERROR" as const;

  constructor(message: string, options?: { cause?: unknown }) {
    super(message, options);
    this.name = "TranscriptFetchError";
  }
}

