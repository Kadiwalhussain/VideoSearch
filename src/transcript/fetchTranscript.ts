export async function fetchTranscript(
  videoId: string
): Promise<TranscriptFetchResult> {
  if (!videoId || !/^[\w-]{6,}$/.test(videoId)) {
    throw new TranscriptFetchError(`Invalid YouTube video id: ${videoId}`);
  }

  console.info("[VideoSearch AI] fetchTranscript: start (innertube ANDROID)", videoId);

  const apiKey = await resolveInnertubeApiKey(videoId);
  console.info("[VideoSearch AI] innertube key:", apiKey.slice(0, 12) + "…");

  const player = await fetchInnertubePlayer(videoId, apiKey);
  assertPlayable(player, videoId);

  const captionsJson = player.captions?.playerCaptionsTracklistRenderer;
  const tracks = (captionsJson?.captionTracks ?? []).filter(
    (t) => typeof t.baseUrl === "string" && t.baseUrl.length > 0
  );

  console.info(
    "[VideoSearch AI] tracks:",
    tracks.length,
    tracks.slice(0, 8).map((t) => ({
      lang: t.languageCode,
      kind: t.kind ?? "manual",
      name: trackDisplayName(t),
      poToken: t.baseUrl.includes("exp=xpe"),
    }))
  );

  if (tracks.length === 0) {
    throw new NoCaptionsError(videoId);
  }

  // Prefer tracks that do NOT require PO token
  const usable = tracks.filter((t) => !t.baseUrl.includes("exp=xpe"));
  const pool = usable.length > 0 ? usable : tracks;

  if (usable.length === 0) {
    console.warn(
      "[VideoSearch AI] All tracks require PO token (exp=xpe). Will try anyway."
    );
  }

  const selected = selectDefaultCaptionTrack(pool);
  // youtube-transcript-api strips a default fmt=srv3 if present
  const baseUrl = selected.baseUrl.replace("&fmt=srv3", "");

  console.info("[VideoSearch AI] selected caption track (native/default):", {
    lang: selected.languageCode,
    kind: selected.kind ?? "manual",
    name: trackDisplayName(selected),
  });

  const { body, usedLabel } = await fetchCaptionBodyRobust(baseUrl);
  console.info(
    "[VideoSearch AI] caption body bytes:",
    body.length,
    "format:",
    usedLabel
  );

  const segments = parseCaptionBody(body);
  if (segments.length === 0) {
    throw new TranscriptFetchError(
      `Parsed 0 segments from caption body (${body.length} bytes, format ${usedLabel}).`
    );
  }

  const trackInfo: CaptionTrackInfo = {
    languageCode: selected.languageCode ?? "und",
    name: trackDisplayName(selected),
    isAutoGenerated: selected.kind === "asr",
    baseUrl,
  };

  const captionTrackHash = await hashCaptionTrack(trackInfo);

  console.info(
    "[VideoSearch AI] fetchTranscript: OK —",
    segments.length,
    "segments"
  );

  return {
    videoId,
    segments,
    captionTrackHash,
    track: trackInfo,
  };
}

